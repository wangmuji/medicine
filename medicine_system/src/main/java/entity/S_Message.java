package entity;


public class S_Message {
	
	private int s_id;
	private String s_time;
	private int id;
	private String name;
	private int s_num;
	private float s_total;
	private String s_reason;
	private float price;
	private String account;
	public int getS_id() {
		return s_id;
	}
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}
	public String getS_time() {
		return s_time;
	}
	public void setS_time(String s_time) {
		this.s_time = s_time;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int s_num) {
		this.s_num = s_num;
	}
	public float getS_total() {
		return s_total;
	}
	public void setS_total(float s_total) {
		this.s_total = s_total;
	}
	public String getS_reason() {
		return s_reason;
	}
	public void setS_reason(String s_reason) {
		this.s_reason = s_reason;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}


}

