package entity;


public class P_Message {
	
	private int p_id;
	private String p_time;
	private int id;
	private String name;
	private int p_num;
	private float p_total;
	private String p_reason;
	private float purchase;
	private String account;
	public int getP_id() {
		return p_id;
	}
	public void setP_id(int p_id) {
		this.p_id = p_id;
	}
	public String getP_time() {
		return p_time;
	}
	public void setP_time(String p_time) {
		this.p_time = p_time;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getP_num() {
		return p_num;
	}
	public void setP_num(int p_num) {
		this.p_num = p_num;
	}
	public float getP_total() {
		return p_total;
	}
	public void setP_total(float p_total) {
		this.p_total = p_total;
	}
	public String getP_reason() {
		return p_reason;
	}
	public void setP_reason(String p_reason) {
		this.p_reason = p_reason;
	}
	public float getPurchase() {
		return purchase;
	}
	public void setPurchase(float purchase) {
		this.purchase = purchase;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
}

